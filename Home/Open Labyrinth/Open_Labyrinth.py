MOVE = {"S": (1, 0), "N": (-1, 0), "W": (0, -1), "E": (0, 1)}

def route_search(x, y, route):
    global result, map
    if result != "":
        return
    for way in MOVE:
        move = MOVE[way]
        tx, ty = x + move[0], y + move[1]
        if tx > 0 and ty > 0 and tx < 12 and ty < 12 and map[tx][ty] != 1:
            route += way
            map[tx][ty] = 1
            if tx == 10 and ty == 10:
                result = route
                return
            route_search(tx, ty, route)
            map[tx][ty] = 0
            route = route[:-1]

def checkio(maze_map):
    global result, map
    result, map = "", maze_map
    route_search(1, 1, "")
    return result


